#include "libft.h"

int	ft_strcmp(char *s1, char *s2)
{
	int	diff;
	int	i;

	i = 0;
	diff = 0;
	while (diff == 0 && ((s2[i] || s1[i])))
	{
		diff = s1[i] - s2[i];
		i++;
	}
	return (diff);
}
